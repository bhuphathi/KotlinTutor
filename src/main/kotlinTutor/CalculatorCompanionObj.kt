class CalculatorCompanionObj() {
    /**
     * Companion Object (direct use of function without instantiate the class)
     * @param a Int
     * @param b Int
     */
    companion object {
        /**
         * Companion Object (direct use of function without instantiate the class)
         * @param a Int
         * @param b Int
         */
        fun sum(a: Int, b: Int): Int {
            return a + b
        }
    }
}
